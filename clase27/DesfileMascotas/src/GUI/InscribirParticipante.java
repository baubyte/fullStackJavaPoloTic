    private void btnInscribirParticipanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscribirParticipanteActionPerformed
       /**Insctanciamos un Participante Vacio*/
        Participante participante = new Participante();
        /**Setamos los Atributos*/
        participante.setApellido(txtApellidoParticipante.getText());
        participante.setNombre(txtNombreParticipante.getText());
        participante.setLocalidad(txtLocalidadParticipante.getText());
        participante.setEdad(Integer.parseInt(txtEdadParticipante.getText()));
       /**Pasamos los datos a la Logica es decir 
        * al Controlador Principal.
        */
       controller.crearParticipante(participante);
    }//GEN-LAST:event_btnInscribirParticipanteActionPerformed


